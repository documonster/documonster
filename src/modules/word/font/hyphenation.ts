/**
 * Hyphenation Engine
 *
 * Implements Liang's hyphenation algorithm (as used in TeX) for automatic
 * word hyphenation without external dependencies.
 *
 * Features:
 * - Pattern-based hyphenation (Liang algorithm)
 * - Built-in patterns for English (US)
 * - Pluggable pattern sets for other languages
 * - Min-prefix / min-suffix constraints
 * - Exception dictionary support
 *
 * @stability experimental
 */

// =============================================================================
// Public API
// =============================================================================

/** Hyphenation options. */
export interface HyphenationOptions {
  /** Minimum characters before the first hyphenation point. Default: 2. */
  readonly minLeft?: number;
  /** Minimum characters after the last hyphenation point. Default: 2. */
  readonly minRight?: number;
  /** Custom hyphen character. Default: "\u00AD" (soft hyphen). */
  readonly hyphenChar?: string;
}

/** Hyphenation pattern set for a language. */
export interface HyphenationPatterns {
  /** Language identifier (e.g., "en-us"). */
  readonly language: string;
  /** Patterns in Liang format (e.g., ".ach4" means level 4 between 'c' and 'h' at word start). */
  readonly patterns: readonly string[];
  /** Exception words with explicit hyphenation (e.g., "as-so-ciate"). */
  readonly exceptions?: readonly string[];
}

/**
 * Create a hyphenator function for a given language/pattern set.
 *
 * @param patterns - The hyphenation patterns to use.
 * @param options - Hyphenation constraints.
 * @returns A function that takes a word and returns an array of hyphenation points (indices).
 */
export function createHyphenator(
  patterns: HyphenationPatterns,
  options?: HyphenationOptions
): (word: string) => number[] {
  const minLeft = options?.minLeft ?? 2;
  const minRight = options?.minRight ?? 2;

  // Build trie from patterns
  const trie = buildTrie(patterns.patterns);

  // Build exception map
  const exceptions = new Map<string, number[]>();
  if (patterns.exceptions) {
    for (const exc of patterns.exceptions) {
      const { word, points } = parseException(exc);
      exceptions.set(word, points);
    }
  }

  return (word: string): number[] => {
    const lower = word.toLowerCase();

    // Check exception dictionary
    const exc = exceptions.get(lower);
    if (exc) {
      return exc.filter(p => p >= minLeft && p <= word.length - minRight);
    }

    // Apply Liang's algorithm
    return computeHyphenPoints(lower, trie, minLeft, minRight);
  };
}

/**
 * Hyphenate a word by inserting soft hyphens at valid break points.
 *
 * @param word - The word to hyphenate.
 * @param hyphenator - A hyphenator function (from createHyphenator).
 * @param hyphenChar - The hyphen character to insert. Default: "\u00AD".
 * @returns The word with soft hyphens inserted.
 */
export function hyphenateWord(
  word: string,
  hyphenator: (w: string) => number[],
  hyphenChar?: string
): string {
  const hChar = hyphenChar ?? "\u00AD";
  const points = hyphenator(word);
  if (points.length === 0) {
    return word;
  }

  let result = "";
  let lastIdx = 0;
  for (const point of points) {
    result += word.slice(lastIdx, point) + hChar;
    lastIdx = point;
  }
  result += word.slice(lastIdx);
  return result;
}

/**
 * Hyphenate all words in a text string.
 *
 * @param text - The text to hyphenate.
 * @param hyphenator - A hyphenator function.
 * @param hyphenChar - The hyphen character to insert.
 * @returns The text with soft hyphens inserted.
 */
export function hyphenateText(
  text: string,
  hyphenator: (w: string) => number[],
  hyphenChar?: string
): string {
  const hChar = hyphenChar ?? "\u00AD";
  // Split on word boundaries while keeping separators
  return text.replace(/[a-zA-Z\u00C0-\u024F]+/g, word => {
    if (word.length < 5) {
      return word; // Skip short words
    }
    const points = hyphenator(word);
    if (points.length === 0) {
      return word;
    }
    let result = "";
    let lastIdx = 0;
    for (const point of points) {
      result += word.slice(lastIdx, point) + hChar;
      lastIdx = point;
    }
    result += word.slice(lastIdx);
    return result;
  });
}

