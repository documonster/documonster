// =============================================================================
// Liang's Algorithm Implementation
// =============================================================================

interface TrieNode {
  children: Map<string, TrieNode>;
  levels: number[] | null; // null if not a pattern end
}

function buildTrie(patterns: readonly string[]): TrieNode {
  const root: TrieNode = { children: new Map(), levels: null };

  for (const pattern of patterns) {
    const { chars, levels } = parsePattern(pattern);

    let node = root;
    for (const ch of chars) {
      let child = node.children.get(ch);
      if (!child) {
        child = { children: new Map(), levels: null };
        node.children.set(ch, child);
      }
      node = child;
    }
    node.levels = levels;
  }

  return root;
}

/**
 * Parse a Liang pattern string into characters and levels.
 * Pattern format: digits interspersed with characters.
 * E.g., ".ach4" → chars=['.','a','c','h'], levels=[0,0,0,4,0]
 */
function parsePattern(pattern: string): { chars: string[]; levels: number[] } {
  const chars: string[] = [];
  const levels: number[] = [0]; // One more level than chars

  for (let i = 0; i < pattern.length; i++) {
    const ch = pattern[i];
    if (ch >= "0" && ch <= "9") {
      levels[levels.length - 1] = parseInt(ch, 10);
    } else {
      chars.push(ch);
      levels.push(0);
    }
  }

  return { chars, levels };
}

/**
 * Parse an exception word (e.g., "as-so-ciate" → word "associate", points [2,4])
 */
function parseException(exc: string): { word: string; points: number[] } {
  const parts = exc.split("-");
  const word = parts.join("");
  const points: number[] = [];
  let pos = 0;
  for (let i = 0; i < parts.length - 1; i++) {
    pos += parts[i].length;
    points.push(pos);
  }
  return { word, points };
}

/**
 * Apply Liang's algorithm to find hyphenation points in a word.
 */
function computeHyphenPoints(
  word: string,
  trie: TrieNode,
  minLeft: number,
  minRight: number
): number[] {
  // Surround with word boundary markers
  const w = "." + word + ".";
  const len = w.length;

  // Initialize levels array (one more than characters)
  const levels = new Array(len + 1).fill(0);

  // Apply all matching patterns
  for (let i = 0; i < len; i++) {
    let node = trie;
    for (let j = i; j < len; j++) {
      const ch = w[j];
      const child = node.children.get(ch);
      if (!child) {
        break;
      }
      node = child;
      if (node.levels) {
        // Apply pattern levels (max with existing)
        for (let k = 0; k < node.levels.length; k++) {
          const pos = i + k;
          if (pos < levels.length && node.levels[k] > levels[pos]) {
            levels[pos] = node.levels[k];
          }
        }
      }
    }
  }

  // Extract break points (odd levels indicate valid breaks)
  // Offset by 1 because of the leading "."
  const points: number[] = [];
  for (let i = minLeft + 1; i <= word.length - minRight; i++) {
    if (levels[i + 1] % 2 === 1) {
      points.push(i);
    }
  }

  return points;
}
