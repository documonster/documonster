/**
 * Stroke font for chart PNG rasterisation.
 *
 * A simplified sans-serif vector font covering ASCII printable characters
 * (32–126). Each glyph is defined as a sequence of polyline strokes in a
 * normalised coordinate system where the cap-height is 1.0 and the
 * em-width is typically 0.6–0.7.
 *
 * Coordinates use a top-left origin with Y increasing downward (matching
 * pixel space). The baseline sits at y = 0.75 (from the top of the em
 * square). Descenders extend below y = 0.75 to approximately y = 1.0.
 *
 * Each glyph entry:
 *   - `w`: advance width (0–1 scale, relative to fontSize)
 *   - `d`: array of strokes, each stroke is an array of [x, y] points
 *
 * This file has NO imports — it is a pure data module.
 */

export interface StrokeGlyph {
  /** Advance width in em units. */
  w: number;
  /** Array of polyline strokes. Each stroke is an array of [x, y] coordinate pairs. */
  d: number[][][];
}

